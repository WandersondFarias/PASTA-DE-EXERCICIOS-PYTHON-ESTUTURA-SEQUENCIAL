
# Programa que calcula a área de um quadrado e mostra o dobro da área

# Pede ao usuário para inserir o lado do quadrado
lado = float(input("Informe o valor do lado do quadrado: "))

# Calcula a área do quadrado
area = lado * lado

# Calcula o dobro da área
dobro_area = 2 * area

# Mostra o dobro da área para o usuário
print(f"O dobro da área do quadrado com lado {lado} é {dobro_area}.")
