# Programa que converte metros para centímetros

# Pede ao usuário para inserir a medida em metros
metros = float(input("Informe a medida em metros: "))

# Converte metros para centímetros
centimetros = metros * 100

# Mostra a medida convertida em centímetros
print(f"A medida em centímetros é {centimetros:.2f} cm.")
