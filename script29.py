# Pedir um valor ao usuário
valor = float(input("Digite um valor: "))

# Verificar se o valor é positivo ou negativo
if valor > 0:
    print("O valor é positivo!")
elif valor < 0:
    print("O valor é negativo!")
else:
    print("O valor é zero!")

