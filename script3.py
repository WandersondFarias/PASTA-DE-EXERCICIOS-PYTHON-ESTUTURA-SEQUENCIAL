# Programa que pede dois números e imprime a soma

# Pede ao usuário para inserir o primeiro número
numero1 = float(input("Por favor, informe o primeiro número: "))

# Pede ao usuário para inserir o segundo número
numero2 = float(input("Por favor, informe o segundo número: "))

# Calcula a soma dos dois números
soma = numero1 + numero2

# Mostra a soma dos dois números
print(f"A soma dos dois números é {soma}.")
